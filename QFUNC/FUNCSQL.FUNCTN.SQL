-- SQL FUNCTION
--   LMi will also create FUNCSQL *SRVPGM CLE

CREATE FUNCTION &LIB/FUNCSQL (YDT CHAR)
   RETURNS DATE
   LANGUAGE SQL
    SPECIFIC FUNCSQL
   BEGIN
   DECLARE F_OUTPUT DATE;
   DECLARE F_TEST INTEGER;
     SET F_TEST =0;
   IF F_TEST=0 THEN
     RETURN F_OUTPUT;
   ELSE
  RETURN NULL;
END IF;
END
